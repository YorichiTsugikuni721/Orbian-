# --- Python PPTX Generator ---
def generate_pptx_bytes(data):
    """Generate a polished .pptx file in memory and return bytes."""
    from pptx import Presentation
    from pptx.util import Inches, Pt
    from pptx.dml.color import RGBColor
    from pptx.enum.text import PP_ALIGN

    t = get_theme(data)
    prs = Presentation()
    prs.slide_width  = Inches(13.33)
    prs.slide_height = Inches(7.5)

    # ── COLOR PALETTE ──────────────────────────────────────────────
    C_DARK   = RGBColor(*t["s"])
    C_ACCENT = RGBColor(*t["p"])
    C_WHITE  = RGBColor(0xFF, 0xFF, 0xFF)
    C_LIGHT  = RGBColor(*t["l"])
    C_TEXT   = RGBColor(*t["t"])

    blank_layout = prs.slide_layouts[6]

    # ── SLIDE 1: TITLE ─────────────────────────────────────────────
    slide = prs.slides.add_slide(blank_layout)
    bg = slide.shapes.add_shape(1, 0, 0, prs.slide_width, prs.slide_height)
    bg.fill.solid(); bg.fill.fore_color.rgb = C_DARK; bg.line.fill.background()

    bar = slide.shapes.add_shape(1, 0, 0, Inches(0.25), prs.slide_height)
    bar.fill.solid(); bar.fill.fore_color.rgb = C_ACCENT; bar.line.fill.background()

    bot = slide.shapes.add_shape(1, 0, Inches(7.0), prs.slide_width, Inches(0.5))
    bot.fill.solid(); bot.fill.fore_color.rgb = C_ACCENT; bot.line.fill.background()

    tx_box = slide.shapes.add_textbox(Inches(0.6), Inches(2.5), Inches(12), Inches(1.5))
    tf = tx_box.text_frame; tf.word_wrap = True
    p = tf.paragraphs[0]; p.alignment = PP_ALIGN.LEFT
    run = p.add_run(); run.text = data.get("title", "Orbian Presentation")
    run.font.bold = True; run.font.size = Pt(44); run.font.color.rgb = C_WHITE

    sub_box = slide.shapes.add_textbox(Inches(0.6), Inches(4.1), Inches(10), Inches(0.8))
    stf = sub_box.text_frame; sp = stf.paragraphs[0]
    sr = sp.add_run(); sr.text = data.get("subtitle", "Generated by Orbian AI")
    sr.font.size = Pt(20); sr.font.color.rgb = C_ACCENT

    foot = slide.shapes.add_textbox(Inches(0.6), Inches(7.05), Inches(12), Inches(0.3))
    ftf = foot.text_frame; fp = ftf.paragraphs[0]; fp.alignment = PP_ALIGN.LEFT
    fr = fp.add_run(); fr.text = f"ORBIAN INTELLIGENCE PLATFORM  •  DESIGN THEME: {data.get('theme','orbian').upper()}"
    fr.font.size = Pt(9); fr.font.color.rgb = C_WHITE; fr.font.bold = True

    # ── CONTENT SLIDES ─────────────────────────────────────────────
    for slide_data in data.get("slides", []):
        slide = prs.slides.add_slide(blank_layout)
        wbg = slide.shapes.add_shape(1, 0, 0, prs.slide_width, prs.slide_height)
        wbg.fill.solid(); wbg.fill.fore_color.rgb = C_LIGHT; wbg.line.fill.background()

        header_bar = slide.shapes.add_shape(1, 0, 0, prs.slide_width, Inches(1.3))
        header_bar.fill.solid(); header_bar.fill.fore_color.rgb = C_DARK; header_bar.line.fill.background()

        acc_stripe = slide.shapes.add_shape(1, 0, Inches(1.1), prs.slide_width, Inches(0.2))
        acc_stripe.fill.solid(); acc_stripe.fill.fore_color.rgb = C_ACCENT; acc_stripe.line.fill.background()

        side_bar = slide.shapes.add_shape(1, Inches(0.4), Inches(1.6), Inches(0.08), Inches(5.4))
        side_bar.fill.solid(); side_bar.fill.fore_color.rgb = C_ACCENT; side_bar.line.fill.background()

        title_box = slide.shapes.add_textbox(Inches(0.5), Inches(0.15), Inches(12), Inches(0.9))
        ttf = title_box.text_frame; tp = ttf.paragraphs[0]; tp.alignment = PP_ALIGN.LEFT
        tr = tp.add_run(); tr.text = slide_data.get("title", "Slide")
        tr.font.bold = True; tr.font.size = Pt(28); tr.font.color.rgb = C_WHITE

        content = slide_data.get("content", [])
        if isinstance(content, str): content = [l.strip() for l in content.split("\n") if l.strip()]

        content_box = slide.shapes.add_textbox(Inches(0.7), Inches(1.6), Inches(11.8), Inches(5.5))
        ctf = content_box.text_frame; ctf.word_wrap = True
        for i, point in enumerate(content):
            cp = ctf.paragraphs[0] if i == 0 else ctf.add_paragraph()
            cp.space_before = Pt(8); cp.space_after = Pt(4)
            cr = cp.add_run(); cr.text = f"▸  {point}"; cr.font.size = Pt(18); cr.font.color.rgb = C_TEXT

        pg_box = slide.shapes.add_textbox(Inches(12.1), Inches(6.9), Inches(1.0), Inches(0.4))
        pgp = pg_box.text_frame.paragraphs[0]; pgp.alignment = PP_ALIGN.RIGHT
        pgr = pgp.add_run(); pgr.text = f"{prs.slides.index(slide)}"
        pgr.font.size = Pt(10); pgr.font.color.rgb = RGBColor(0x94, 0xa3, 0xb8)

    buf = io.BytesIO()
    prs.save(buf)
    return buf.getvalue()


# ─── PDF GENERATOR (reportlab) ────────────────────────────────────────────────
def generate_pdf_bytes(data):
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.lib.units import cm
    from reportlab.lib import colors
    from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, HRFlowable, Table, TableStyle
    from reportlab.lib.enums import TA_LEFT, TA_CENTER

    t = get_theme(data)
    NAVY   = colors.HexColor('#' + t["hs"])
    INDIGO = colors.HexColor('#' + t["hp"])
    SLATE  = colors.HexColor('#' + t["hs"]) # Using secondary for prominence
    LIGHT  = colors.HexColor('#' + (t["l"] if isinstance(t["l"], str) else "".join("%02x"%c for c in t["l"])))
    WHITE  = colors.white

    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=A4, leftMargin=2*cm, rightMargin=2*cm, topMargin=2.5*cm, bottomMargin=2*cm)
    styles = getSampleStyleSheet()

    title_style = ParagraphStyle('OrbianTitle', fontName='Helvetica-Bold', fontSize=28, textColor=WHITE, leading=34, alignment=TA_LEFT, spaceAfter=6, backColor=NAVY)
    h2_style = ParagraphStyle('OrbianH2', fontName='Helvetica-Bold', fontSize=16, textColor=INDIGO, leading=20, spaceBefore=20, spaceAfter=8)
    body_style = ParagraphStyle('OrbianBody', fontName='Helvetica', fontSize=11, textColor=colors.HexColor('#334155'), leading=18, spaceAfter=6, leftIndent=12)
    bullet_style = ParagraphStyle('OrbianBullet', fontName='Helvetica', fontSize=11, textColor=colors.HexColor('#334155'), leading=18, spaceAfter=5, leftIndent=20, bulletIndent=8, bulletText='\u25b8')
    subtitle_style = ParagraphStyle('OrbianSub', fontName='Helvetica', fontSize=13, textColor=INDIGO, leading=18, spaceAfter=4)

    elements = []
    title_table = Table([[Paragraph(data.get('title','Orbian Document'), title_style)]], colWidths=[16.9*cm])
    title_table.setStyle(TableStyle([('BACKGROUND', (0,0), (-1,-1), NAVY), ('TOPPADDING', (0,0), (-1,-1), 22), ('BOTTOMPADDING', (0,0), (-1,-1), 22), ('LEFTPADDING', (0,0), (-1,-1), 20), ('RIGHTPADDING', (0,0), (-1,-1), 20)]))
    elements.append(title_table)
    elements.append(Spacer(1, 0.3*cm))
    elements.append(Paragraph(data.get('subtitle', 'Generated by Orbian AI'), subtitle_style))
    elements.append(HRFlowable(width='100%', thickness=2, color=INDIGO, spaceAfter=16))

    for i, slide in enumerate(data.get('slides', []), 1):
        elements.append(Paragraph(f"{i}. {slide.get('title','')}", h2_style))
        elements.append(HRFlowable(width='100%', thickness=0.5, color=colors.HexColor('#e2e8f0'), spaceAfter=8))
        content = slide.get('content', [])
        if isinstance(content, str): content = [l.strip() for l in content.split('\n') if l.strip()]
        for point in content: elements.append(Paragraph(point, bullet_style))
        elements.append(Spacer(1, 0.4*cm))

    elements.append(Spacer(1, 1*cm)); elements.append(HRFlowable(width='100%', thickness=1, color=INDIGO)); elements.append(Spacer(1, 0.2*cm))
    footer_style = ParagraphStyle('Footer', fontName='Helvetica', fontSize=8, textColor=colors.HexColor('#94a3b8'), alignment=TA_CENTER)
    elements.append(Paragraph(f'ORBIAN INTELLIGENCE PLATFORM  \u2022  THEME: {data.get("theme","ORBIAN").upper()}', footer_style))

    doc.build(elements)
    return buf.getvalue()


# ─── WORD GENERATOR (python-docx) ─────────────────────────────────────────────
def generate_docx_bytes(data):
    from docx import Document
    from docx.shared import Pt, Cm, RGBColor as DocxRGB
    from docx.enum.text import WD_ALIGN_PARAGRAPH
    from docx.oxml.ns import qn
    from docx.oxml import OxmlElement

    t = get_theme(data); doc = Document()
    for s in doc.sections: s.top_margin=Cm(2.5); s.bottom_margin=Cm(2); s.left_margin=Cm(2.5); s.right_margin=Cm(2.5)

    def set_para_bg(para, hex_color):
        pPr = para._p.get_or_add_pPr()
        shd = OxmlElement('w:shd'); shd.set(qn('w:val'), 'clear'); shd.set(qn('w:color'), 'auto'); shd.set(qn('w:fill'), hex_color)
        pPr.append(shd)

    def add_r(para, text, bold=False, size=11, rgb=(255,255,255)):
        run = para.add_run(text); run.bold = bold; run.font.size = Pt(size); run.font.color.rgb = DocxRGB(*rgb); return run

    title_p = doc.add_paragraph(); title_p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    set_para_bg(title_p, t["hs"]); add_r(title_p, data.get('title','Orbian Document'), bold=True, size=26)
    sub_p = doc.add_paragraph(); sub_p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    set_para_bg(sub_p, t["hs"]); add_r(sub_p, data.get('subtitle','Generated by Orbian AI'), size=13, rgb=t["p"])
    doc.add_paragraph()

    for i, slide in enumerate(data.get('slides', []), 1):
        h = doc.add_heading(f"{i}. {slide.get('title','')}", level=2)
        h.runs[0].font.color.rgb = DocxRGB(*t["p"]); h.runs[0].font.size = Pt(14)
        content = slide.get('content', []);
        if isinstance(content, str): content = [l.strip() for l in content.split('\n') if l.strip()]
        for point in content:
            p = doc.add_paragraph(style='List Bullet'); p.paragraph_format.left_indent = Cm(0.5)
            r = p.add_run(point); r.font.size = Pt(11); r.font.color.rgb = DocxRGB(51, 65, 85)
        doc.add_paragraph()

    f_p = doc.add_paragraph(); f_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    add_r(f_p, f'ORBIAN INTELLIGENCE PLATFORM  \u2022  THEME: {data.get("theme","orbian").upper()}', size=8, rgb=(148, 163, 184))

    buf = io.BytesIO(); doc.save(buf); return buf.getvalue()


