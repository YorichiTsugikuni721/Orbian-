class CORSProxyRequestHandler(http.server.SimpleHTTPRequestHandler):
    def do_GET(self):
        # Handle Proxy for Sketchfab
        if self.path.startswith('/api/sketchfab/search'):
            query_string = self.path.split('?')[-1] if '?' in self.path else ''
            target_url = f"https://api.sketchfab.com/v3/search?{query_string}"
            self.handle_proxy_request(target_url)
            return

        # Handle Proxy for Perplexity
        if self.path.startswith('/api/proxy/perplexity'):
            target_url = "https://api.perplexity.ai/chat/completions"
            self.handle_proxy_post(target_url)
            return

        # Handle Proxy for Gemini
        if self.path.startswith('/api/proxy/gemini'):
            # Extracts the full target URL from the query or just appends the query
            parsed = urllib.parse.urlparse(self.path)
            query = parsed.query
            target_url = f"https://generativelanguage.googleapis.com/{parsed.path.replace('/api/proxy/gemini/', '')}?{query}"
            self.handle_proxy_post(target_url)
            return

        # Handle Proxy for Groq
        if self.path.startswith('/api/proxy/groq'):
            target_url = "https://api.groq.com/openai/v1/chat/completions"
            self.handle_proxy_post(target_url)
            return

        # Handle Proxy for OpenRouter
        if self.path.startswith('/api/proxy/openrouter'):
            target_url = "https://openrouter.ai/api/v1/chat/completions"
            self.handle_proxy_post(target_url)
            return
        
        # Handle Favicon specifically
        if self.path == '/favicon.ico':
            self.path = '/logo.png'
            return super().do_GET()

        # Handle extensionless HTML URLs
        parsed = urllib.parse.urlparse(self.path)
        path = parsed.path
        if not path.endswith('/'):
            _, ext = os.path.splitext(path)
            if not ext:
                if os.path.exists(self.translate_path(path + '.html')):
                    self.path = path + '.html' + ('?' + parsed.query if parsed.query else '')

        # Standard serve
        super().do_GET()

    def guess_type(self, path):
        """Override MIME types for PWA files."""
        if str(path).endswith('manifest.json'):
            return 'application/manifest+json'
        if str(path).endswith('sw.js'):
            return 'application/javascript'
        return super().guess_type(path)

    def do_OPTIONS(self):
        """Handle CORS preflight for the PPT API."""
        self.send_response(200)
        self.send_header('Access-Control-Allow-Origin', '*')
        self.send_header('Access-Control-Allow-Methods', 'POST, OPTIONS')
        self.send_header('Access-Control-Allow-Headers', 'Content-Type')
        self.end_headers()

    def do_POST(self):
        """Handle POST /api/generate-* document endpoints."""

        ENDPOINTS = {
            '/api/generate-ppt': {
                'fn': generate_pptx_bytes,
                'ext': 'pptx',
                'mime': 'application/vnd.openxmlformats-officedocument.presentationml.presentation',
            },
            '/api/generate-pdf': {
                'fn': generate_pdf_bytes,
                'ext': 'pdf',
                'mime': 'application/pdf',
            },
            '/api/generate-docx': {
                'fn': generate_docx_bytes,
                'ext': 'docx',
                'mime': 'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
            },
            '/api/generate-xlsx': {
                'fn': generate_xlsx_bytes,
                'ext': 'xlsx',
                'mime': 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet',
            },
        }

        endpoint = ENDPOINTS.get(self.path)
        if endpoint:
            try:
                length = int(self.headers.get('Content-Length', 0))
                raw = self.rfile.read(length)
                data = json.loads(raw.decode('utf-8'))

                file_bytes = endpoint['fn'](data)

                title = data.get('title', 'Orbian_Document')
                safe_title = "".join(c if c.isalnum() or c in ' _-' else '_' for c in title).strip().replace(' ', '_')
                filename = f"{safe_title}.{endpoint['ext']}"

                self.send_response(200)
                self.send_header('Content-Type', endpoint['mime'])
                self.send_header('Content-Disposition', f'attachment; filename="{filename}"')
                self.send_header('Content-Length', str(len(file_bytes)))
                self.send_header('Access-Control-Allow-Origin', '*')
                self.end_headers()
                self.wfile.write(file_bytes)

            except Exception as e:
                import traceback
                err_msg = json.dumps({"error": str(e), "trace": traceback.format_exc()}).encode()
                self.send_response(500)
                self.send_header('Content-Type', 'application/json')
                self.send_header('Access-Control-Allow-Origin', '*')
                self.send_header('Content-Length', str(len(err_msg)))
                self.end_headers()
                self.wfile.write(err_msg)
        else:
            self.send_error(404, "Not Found")

    def translate_path(self, path):
        # 1. Split out query/fragment
        path_only = path.split('?')[0].split('#')[0]
        
        # 2. SPA Routing for AI Chat: /app/anything -> app.html
        # If it's a deep path under /app and NOT a file request
        if path_only.startswith('/app/') and '.' not in os.path.basename(path_only):
            return os.path.join(os.getcwd(), 'app.html')

        # 3. Standard Translation
        translated = super().translate_path(path_only)
        
        # 4. Extensionless HTML (e.g. /auth/login -> /auth/login.html)
        if not os.path.exists(translated) and not os.path.isdir(translated):
            if '.' not in os.path.basename(path_only):
                html_path = translated + '.html'
                if os.path.exists(html_path):
                    return html_path

        # 5. Asset Fallback for SPA (e.g. /app/styles.css -> /styles.css)
        if not os.path.exists(translated):
            parts = [p for p in path_only.split('/') if p]
            if len(parts) >= 2:
                # Try finding the file in root by removing the first directory
                alt_relative = '/'.join(parts[1:])
                alt_translated = super().translate_path('/' + alt_relative)
                if os.path.exists(alt_translated):
                    return alt_translated

        return translated

    def handle_proxy_request(self, target_url):
        try:
            req = urllib.request.Request(target_url)
            # Add user agent to avoid blocking
            req.add_header('User-Agent', 'Mozilla/5.0') 

            with urllib.request.urlopen(req) as response:
                self.send_response(response.status)
                self.send_header('Content-Type', 'application/json')
                self.send_header('Access-Control-Allow-Origin', '*')
                self.end_headers()
                self.wfile.write(response.read())

        except urllib.error.HTTPError as e:
            self.send_response(e.code)
            self.send_header('Content-Type', 'application/json')
            self.send_header('Access-Control-Allow-Origin', '*')
            self.end_headers()
            self.wfile.write(e.read())
        except Exception as e:
            self.send_error(500, f"Internal Proxy Error: {str(e)}")

    def handle_proxy_post(self, target_url):
        try:
            length = int(self.headers.get('Content-Length', 0))
            body = self.rfile.read(length)
            
            req = urllib.request.Request(target_url, data=body, method='POST')
            
            # Forward relevant headers
            for header in ['Authorization', 'Content-Type', 'x-goog-api-client', 'x-goog-api-key']:
                if header in self.headers:
                    req.add_header(header, self.headers[header])
            
            # Add user agent
            req.add_header('User-Agent', 'Mozilla/5.0')

            with urllib.request.urlopen(req) as response:
                self.send_response(response.status)
                # Forward response headers (simplified)
                self.send_header('Content-Type', 'application/json')
                self.send_header('Access-Control-Allow-Origin', '*')
                self.end_headers()
                self.wfile.write(response.read())

        except urllib.error.HTTPError as e:
            self.send_response(e.code)
            self.send_header('Content-Type', 'application/json')
            self.send_header('Access-Control-Allow-Origin', '*')
            self.end_headers()
            self.wfile.write(e.read())
        except Exception as e:
            import traceback
            print(traceback.format_exc())
            self.send_error(500, f"Internal Proxy Error: {str(e)}")

def serve():
    import socket
    Handler = CORSProxyRequestHandler
    socketserver.TCPServer.allow_reuse_address = True

    # Get local network IP for phone access
    try:
        local_ip = socket.gethostbyname(socket.gethostname())
    except:
        local_ip = '192.168.x.x'

    with socketserver.TCPServer(("", PORT), Handler) as httpd:
        print(f"[OK] Orbian Server started!")
        print(f"     PC:    http://localhost:{PORT}")
        print(f"     Phone: http://{local_ip}:{PORT}  (same WiFi required)")
        print("[OK] CORS Proxy + PDF/DOCX/XLSX/PPTX API Active")
        print("[PWA] manifest.json + sw.js ready for install")
        print("Press Ctrl+C to stop.")
        
        webbrowser.open(f"http://localhost:{PORT}/")
        
        try:
            httpd.serve_forever()
        except KeyboardInterrupt:
            print("\nServer stopped.")
            sys.exit(0)

if __name__ == "__main__":
    serve()
